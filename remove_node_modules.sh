#!/bin/bash

echo "Removing node_modules files from git tracking..."

# Get all node_modules files and remove them from git index in batches
git ls-files | grep "alebrije-wallet/node_modules" | while read -r file; do
    git rm --cached "$file" 2>/dev/null
done

echo "Finished removing node_modules from git tracking"
echo "Checking remaining count..."
git ls-files | grep "alebrije-wallet/node_modules" | wc -l 